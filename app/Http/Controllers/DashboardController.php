     */
    public function index()
    {
        $currentTime = Carbon::now();
        $hour = $currentTime->hour;

        if ($hour >= 5 && $hour <= 11) {
            $greeting = 'Selamat pagi';
        } elseif ($hour >= 12 && $hour <= 17) {
            $greeting = 'Selamat sore';
        } else {
            $greeting = 'Selamat malam';
        }

        return view("dashboard.index", [
            "title" => "Dashboard",
            "greeting" => $greeting,
        ]);
    }

